function add(x, y) {
  return x + y;
}
function substract(x, y) {
  return x - y;
}
function multiply(x, y) {
  return x * y;
}
function divide(x, y) {
  return x / y;
}

function squareRoot(x) {
  return Math.sqrt(x);
}
module.exports = { add, substract, multiply };
module.exports = { squareRoot };
